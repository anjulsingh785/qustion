package basic;



public class CW04_Literals {
    public static void main(String[] args) {
        byte age = 45;
        int age2 = 56;
        short age3 = 7;
        long ageDino = 6744444444444444444L;   //On long datatype use suffix l and L
        char ch = 'A';  // in char we only write in single quote
        float f1 = 5.6f;    //  in float datatype suffix f or F used
        double d1 = 5.69;  // in double datatype D OR d understood by default
        boolean a = true;
        System.out.println(age);
        String str = "Anjul";
        System.out.println(str);

    }
}
